     private void addTriptoTable(){
        try{
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Registry registry= LocateRegistry.getRegistry("127.0.0.1",7000);
            TripService service=(TripService)registry.lookup("trip");
            List<Trip> allTrips= service.allTrips();
            Iterator iterator= allTrips.iterator();
            while(iterator.hasNext()){
                
                Trip trip=(Trip)iterator.next();
                String formattedDate = sdf.format(trip.getTrip_date());
                Object[] row = {
            trip.getTrip_id(),
            trip.getDestination(),
            trip.getAmount(),
            trip.getStatus(),
            formattedDate
                    
    };
    tablemodel.addRow(row);
            }
            TripTb.setModel(tablemodel);
            
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }
    

