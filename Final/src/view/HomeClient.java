    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    DefaultTableModel tablemodel = new DefaultTableModel();
    tablemodel.addColumn("ID");
    tablemodel.addColumn("Destination");
    tablemodel.addColumn("Name");
    tablemodel.addColumn("Date");
    
    bookTb.setModel(tablemodel);
    
    if (txtname.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a name to search.");
        return;
    }
    
    // Connect to RMI registry and fetch user data by ID
    Registry registry = LocateRegistry.getRegistry("127.0.0.1", 7000);
    ClientService service = (ClientService) registry.lookup("client");
    
    // Get all clients with the given name
    List<Client> clients = service.getClientsByName(txtname.getText());
    
    if (clients != null && !clients.isEmpty()) {
        // Add each client to the table model
        for (Client client : clients) {
            String formattedDate = sdf.format(client.getTrip_date());
            Object[] row = {
                client.getId(),
                client.getClient_trip(),
                client.getClient_name(),
                formattedDate
            };
            tablemodel.addRow(row);
        }
    } else {
        JOptionPane.showMessageDialog(this, "No clients found with the given name.");
    }

} catch(Exception ex) {
    ex.printStackTrace();
    JOptionPane.showMessageDialog(this, "Error occurred while searching: " + ex.getMessage());
}
    }//GEN-LAST:event_jButton3ActionPerformed

